package com.example.majiang.dto;

public class GithubUser {
    private int id;
    private String name;
    private String tupe;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTupe() {
        return tupe;
    }

    public void setTupe(String tupe) {
        this.tupe = tupe;
    }
}
